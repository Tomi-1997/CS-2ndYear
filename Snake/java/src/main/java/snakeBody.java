public class snakeBody {
    public int i;
    public int j;
    public snakeBody(int i, int j) {
        setI(i);
        setJ(j);
    }
    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int getJ() {
        return j;
    }

    public void setJ(int j) {
        this.j = j;
    }



}
